// Variables in JavaScript

// "let" = changeable variable
let name = "Sean";
console.log("My name is:", name);

// "const" = fixed variable (cannot be reassigned)
const pi = 3.14159;
console.log("The value of pi is:", pi);

// Reassigning a let
name = "Pubspotter";
console.log("Now my name is:", name);

// Old style (var) - not recommended
var legacy = "I am var";
console.log(legacy);